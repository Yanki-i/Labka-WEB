//Створити функцію, яка за номером місяця повертає його назву.

month = prompt('Введіть номер місяця');
switch(month){
    case '1':
        alert('Січень');
        break;
    case '2':
        alert('Лютий');
        break;
    case '3':
        alert('Березень');
        break;
    case '4':
        alert('Квітень');
        break;
    case '5':
        alert('Травень');
        break;
    case '6':
        alert('Червень');
        break;
    case '7':
        alert('Липень');
        break;
    case '8':
        alert('Серпень');
        break;
    case '9':
        alert('Вересень');
        break;
    case '10':
        alert('Жовтень');
        break;
    case '11':
        alert('Листопад');
        break;
    case '12':
        alert('Грудень');
        break;                                               
    default:
        alert('Введіть число місяць від 1-12');   
}    
